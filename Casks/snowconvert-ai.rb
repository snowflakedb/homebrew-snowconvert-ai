  desc "AI-powered CLI tool for automated code migration to Snowflake"
  homepage "https://docs.snowflake.com/en/migrations/snowconvert-docs/overview"
  version "2.1.0-rc.141"

  arch_suffix = Hardware::CPU.intel? ? "x64" : "arm64"

